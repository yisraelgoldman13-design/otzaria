  /// Get all book names for a category
  static List<String> getBookNamesForCategory(String category) {
    return builtInBookPaths[category]?.keys.toList() ?? [];
  }

  /// Get path for a specific book
  static String? getBookPath(String category, String bookName) {
    return builtInBookPaths[category]?[bookName];
  }

  /// Check if a book is built-in
  static bool isBuiltInBook(String category, String bookName) {
    return builtInBookPaths[category]?.containsKey(bookName) ?? false;
  }

  /// Get all built-in book entries as a flat list
  static List<MapEntry<String, String>> getAllBuiltInBooks() {
    final List<MapEntry<String, String>> result = [];
    builtInBookPaths.forEach((category, books) {
      books.forEach((bookName, path) {
        result.add(MapEntry('$category:$bookName', path));
      });
    });
    return result;
  }
